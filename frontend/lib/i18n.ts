export type TranslationKey = keyof typeof translations.ko;

export function t(lang: Lang, key: TranslationKey, vars?: Record<string, string | number>): string {
  let str: string = (translations[lang] as Record<string, string>)[key] ?? (translations.ko as Record<string, string>)[key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(`{${k}}`, String(v));
    });
  }
  return str;
}

/** Warmth level label translation helper */
export function getTensionLevelLabel(level: 0 | 1 | 2 | 3, lang: Lang): string {
  const keys = ["tension_level_0", "tension_level_1", "tension_level_2", "tension_level_3"] as const;
  return t(lang, keys[level]);
}
